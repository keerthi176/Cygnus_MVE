/* LTDC init function */
void MX_LTDC_Init(void)
{
  hltdc.Instance = LTDC;
  hltdc.Init.HSPolarity = LTDC_HSPOLARITY_AH;
  hltdc.Init.VSPolarity = LTDC_VSPOLARITY_AH;
  hltdc.Init.DEPolarity = LTDC_DEPOLARITY_AL;
  hltdc.Init.PCPolarity = LTDC_PCPOLARITY_IPC;

#if VGA_MODE==0
  hltdc.Init.HorizontalSync = 20;
  hltdc.Init.VerticalSync = 20;
  hltdc.Init.AccumulatedHBP = 46 + 20;
  hltdc.Init.AccumulatedVBP = 23 + 20;
  hltdc.Init.AccumulatedActiveW = 800 + 46 + 20;
  hltdc.Init.AccumulatedActiveH = 480 + 23 + 20;
  hltdc.Init.TotalWidth = 800 + 46 + 20 + 210;
  hltdc.Init.TotalHeigh = 480 + 23 + 20 + 120;
#else
  hltdc.Init.HorizontalSync = 128;
  hltdc.Init.VerticalSync = 4;
  hltdc.Init.AccumulatedHBP = 128 + 88;
  hltdc.Init.AccumulatedVBP = 4 + 23;
  hltdc.Init.AccumulatedActiveW = 128 + 88 + 800;
  hltdc.Init.AccumulatedActiveH = 4 + 23 + 600;
  hltdc.Init.TotalWidth = 128 + 88 + 800 + 40;
  hltdc.Init.TotalHeigh = 4 + 23 + 600 + 1;	
#endif	
  hltdc.Init.Backcolor.Blue = 0;
  hltdc.Init.Backcolor.Green = 0;
  hltdc.Init.Backcolor.Red = 0;
  if (HAL_LTDC_Init(&hltdc) != HAL_OK)
  {
    _Error_Handler(__FILE__, __LINE__);
  }

  pLayerCfg.WindowX0 = 0;
  pLayerCfg.WindowX1 = 800;
#if VGA_MODE==0  
  pLayerCfg.WindowY0 = 0;
  pLayerCfg.WindowY1 = 480;
#else  
  pLayerCfg.WindowY0 = 60;
  pLayerCfg.WindowY1 = 600-60;
#endif  
  pLayerCfg.PixelFormat = LTDC_PIXEL_FORMAT_RGB565;
  pLayerCfg.Alpha = 24;
  pLayerCfg.Alpha0 = 0;
  pLayerCfg.BlendingFactor1 = LTDC_BLENDING_FACTOR1_CA;
  pLayerCfg.BlendingFactor2 = LTDC_BLENDING_FACTOR2_CA;
  pLayerCfg.FBStartAdress = 0xC0000000;
  pLayerCfg.ImageWidth = 800;
  pLayerCfg.ImageHeight = 480;
  pLayerCfg.Backcolor.Blue = 0;
  pLayerCfg.Backcolor.Green = 0;
  pLayerCfg.Backcolor.Red = 0;
  if (HAL_LTDC_ConfigLayer(&hltdc, &pLayerCfg, 0) != HAL_OK)
  {
    _Error_Handler(__FILE__, __LINE__);
  }
}


